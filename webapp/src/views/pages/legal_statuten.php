  <h2>§ 1 Name, Sitz und Tätigkeitsbereich</h2>

  <h3>1.1 Name</h3>
  <p>Der Verein führt den Namen „Erneuerbare-Energie-Gemeinschaft Strompool Feldkirchen Süd-West".</p>

  <h3>1.2 Sitz</h3>
  <p>Der Verein hat seinen Sitz in der Stadtgemeinde Feldkirchen in Kärnten, politischer Bezirk Feldkirchen, Land Kärnten.</p>

  <h3>1.3 Tätigkeitsbereich</h3>
  <p>Der Tätigkeitsbereich des Vereins erstreckt sich schwerpunktmäßig auf das Gebiet des Bundeslandes Kärnten, insbesondere den Bezirk Feldkirchen.</p>
  <p>Der Tätigkeitsbereich ist durch § 16c Abs 2 ElWOG 2010 beschränkt. Der Verein ist als regionale Erneuerbare-Energie-Gemeinschaft (Regionale ID: 23R1) auf Netzebene 5 (Mittelspannung) im Konzessionsgebiet der Kärnten Netz GmbH tätig.</p>
  <p>Die Errichtung von Zweigvereinen ist nicht beabsichtigt.</p>

  <h2>§ 2 Vereinszweck und Ziele</h2>

  <h3>2.1 Politische und religiöse Unabhängigkeit</h3>
  <p>Der Verein ist nicht auf Gewinn, sondern nur auf ideelle Ziele ausgerichtet und verfolgt keine politischen oder religiösen Ziele.</p>

  <h3>2.2 Zweck des Vereins</h3>
  <p>Der Vereinszweck umfasst unter Berücksichtigung ökologischer, gemeinwirtschaftlicher und sozialgemeinschaftlicher Zielsetzungen (§ 79 Abs 2 EAG):</p>
  <ol>
    <li>gemeinsame Erzeugung und Nutzung von Energie aus erneuerbaren Quellen, insbesondere Photovoltaik;</li>
    <li>Verbrauch eigenerzeugter Energie durch die Mitglieder;</li>
    <li>nicht gewinnorientierter Verkauf von Energie innerhalb der Energiegemeinschaft;</li>
    <li>Förderung der Erzeugung von Energie aus erneuerbaren Energiequellen.</li>
  </ol>
  <p>Der Hauptzweck des Vereins ist – unbeschadet der zwingenden Beschränkungen des § 1 Abs 2 VerG – nicht auf finanziellen Gewinn (§ 79 Abs 2 EAG) gerichtet.</p>
  <p>Der Verein ist NICHT gemeinnützig im Sinne der §§ 34 ff BAO (vgl. BMF-Auskunft vom 20.12.2021, GZ 2021-0.893.702).</p>

  <h3>2.3 Abgrenzung</h3>
  <p>Der Verein ist als Überschusseinspeiser-Gemeinschaft konzipiert. Innerhalb der EEG wird ausschließlich jene Energie verteilt, die von erzeugenden Mitgliedern über ihre Photovoltaikanlagen erzeugt und nicht selbst verbraucht wird.</p>

  <h2>§ 3 Mittel zur Erreichung des Vereinszwecks</h2>

  <h3>3.1 Ideelle Mittel</h3>
  <ul>
    <li>Information und Diskussion zu Klima- und Umweltschutzthemen;</li>
    <li>Information und Beratung zu Energiesparen und Energieeffizienz;</li>
    <li>Sammlung und Weitergabe von Informationen zur Energiewende.</li>
  </ul>

  <h3>3.2 Materielle Mittel</h3>
  <ul>
    <li>Mitgliedsbeiträge (Höhe vom Vorstand festgelegt);</li>
    <li>Erlöse aus dem Verkauf von Energie innerhalb der EEG (Differenz zwischen Verbrauchertarif und Erzeugertarif);</li>
    <li>Subventionen und Förderungen, insbesondere nach § 80 EAG;</li>
    <li>Spenden, Schenkungen und sonstige Zuwendungen.</li>
  </ul>

  <h2>§ 4 Mittelverwendung</h2>
  <p>Die Einnahmen des Vereins stehen ausschließlich Zwecken der Verwirklichung der Vereinsziele zur Verfügung. Der Verein erstrebt in seinem Hauptzweck keinen finanziellen Gewinn (§ 79 Abs 2 EAG).</p>
  <p>Die Differenz zwischen Verbraucher- und Erzeugertarif dient ausschließlich der Deckung von:</p>
  <ul>
    <li>Verwaltung, Buchhaltung, Steuerberatung;</li>
    <li>externen Dienstleistungen zur Abwicklung der EEG;</li>
    <li>Steuern, Abgaben und sonstigen laufenden Aufwendungen;</li>
    <li>Liquiditätsreserven und Rücklagen für unvorhergesehene Ausgaben.</li>
  </ul>
  <p>Die Mitglieder dürfen keine Gewinnanteile und in ihrer Eigenschaft als Mitglieder keine sonstigen Zuwendungen aus Mitteln des Vereins erhalten. Eine Gewinnausschüttung an Mitglieder findet nicht statt.</p>

  <h2>§ 5 Arten der Mitgliedschaft</h2>
  <p>Die Mitglieder gliedern sich in:</p>
  <ul>
    <li>Ordentliche Mitglieder: natürliche oder juristische Personen mit Berechtigung als teilnehmende Netzbenutzer im Sinne des § 16d Abs 1 ElWOG 2010, die in der EEG entweder Energie erzeugen, verbrauchen oder beides;</li>
    <li>Außerordentliche Mitglieder: Fördermitglieder, die die Vereinstätigkeit ohne Energiebezug unterstützen;</li>
    <li>Ehrenmitglieder: natürliche Personen, die wegen besonderer Verdienste um den Verein ernannt werden.</li>
  </ul>
  <p>Die Mehrfachmitgliedschaft in mehreren Energiegemeinschaften (EEG, BEG, GEA) gemäß § 111 Abs 8 ElWOG 2010 ist zulässig.</p>

  <h2>§ 6 Erwerb der Mitgliedschaft</h2>

  <h3>6.1 Berechtigung</h3>
  <p>Die Berechtigung zur ordentlichen Mitgliedschaft richtet sich nach § 79 Abs 2 EAG sowie § 16c Abs 1 ElWOG 2010. Großunternehmen sind als ordentliche Mitglieder ausgeschlossen.</p>

  <h3>6.2 Gründungsmitglieder</h3>
  <p>Gründungsmitglieder sind:</p>
  <ul>
    <li>Amlacher Fabian</li>
    <li>Brunner Alexander</li>
    <li>Ropper Patrick</li>
  </ul>

  <h3>6.3 Aufnahme</h3>
  <p>Über die Aufnahme neuer Mitglieder entscheidet ausschließlich der Vorstand. Die Aufnahme erfolgt durch schriftlichen Aufnahmeantrag des Bewerbers und durch Vorstandsbeschluss. Eine Begründung des Vorstands ist im Fall der Ablehnung schriftlich mitzuteilen.</p>
  <p>Eine Grundeinlage und ein Eintrittsgeld werden nicht erhoben.</p>
  <p>Mit der Aufnahme erkennt das Mitglied die Statuten sowie die jeweils gültigen Beschlüsse des Vorstandes über Mitgliedsbeiträge und Energiepreise an.</p>

  <h2>§ 7 Beendigung der Mitgliedschaft</h2>

  <h3>7.1 Erlöschen</h3>
  <p>Die Mitgliedschaft erlischt durch:</p>
  <ul>
    <li>Tod eines Mitglieds (siehe Abs. 7.2);</li>
    <li>bei juristischen Personen durch Verlust der Rechtspersönlichkeit;</li>
    <li>Verlust der Mitgliedschaftsvoraussetzungen nach § 79 Abs 2 EAG sowie § 16c Abs 1 ElWOG 2010 (insbesondere durch Wegzug aus dem Konzessionsgebiet);</li>
    <li>freiwilligen Austritt;</li>
    <li>Ausschluss durch den Vorstand.</li>
  </ul>

  <h3>7.2 Rechtsnachfolge im Todesfall</h3>
  <p>Im Falle des Todes eines ordentlichen Mitglieds geht die Mitgliedschaft, soweit rechtlich zulässig, auf den/die Rechtsnachfolger:in im Eigentum der Verbrauchs- bzw. Erzeugungsanlage über. Ist eine unmittelbare Rechtsnachfolge rechtlich nicht zulässig, hat der/die Rechtsnachfolger:in im Eigentum der Anlage das Recht, binnen 2 Monaten ab dem Tod des Mitglieds durch einseitige Erklärung die ordentliche Mitgliedschaft zu übernehmen.</p>

  <h3>7.3 Austritt</h3>
  <p>Der Austritt eines Mitglieds kann mit einmonatiger Kündigungsfrist zum Monatsletzten erfolgen, sofern für Verbraucher im Sinne des § 1 Abs 1 Z 2 KSchG und Kleinunternehmen nicht kürzere Austrittsfristen gemäß § 76 Abs 1 ElWOG 2010 zwingend zur Anwendung gelangen.</p>
  <p>Der Austritt ist schriftlich (E-Mail zulässig) gegenüber dem Vorstand zu erklären.</p>

  <h3>7.4 Mitgliedsbeiträge bei Austritt</h3>
  <p>Bereits geleistete Mitgliedsbeiträge verbleiben bei unterjährigem Austritt beim Verein und werden nicht rückerstattet. Offene Energieverbräuche sind bis zum Austrittsdatum vollständig zu begleichen.</p>

  <h3>7.5 Ausschluss</h3>
  <p>Der Vorstand kann ein Mitglied bei Vorliegen eines wichtigen Grundes ausschließen. Wichtige Gründe sind insbesondere:</p>
  <ul>
    <li>Zahlungsverzug über 3 Monate nach zweimaliger schriftlicher Mahnung;</li>
    <li>grobe Verletzung der Mitgliedspflichten;</li>
    <li>vereinsschädigendes Verhalten;</li>
    <li>Wegfall der Mitgliedschaftsvoraussetzungen.</li>
  </ul>
  <p>Das betroffene Mitglied ist vor dem Ausschluss anzuhören. Der Ausschluss ist dem Mitglied schriftlich und begründet mitzuteilen. Dem ausgeschlossenen Mitglied steht binnen 4 Wochen ab Zustellung das Recht zur Anrufung der Schlichtungseinrichtung gemäß § 16 dieser Statuten zu. Bis zur endgültigen Entscheidung der Schlichtungseinrichtung ruhen die Mitgliedsrechte.</p>

  <h2>§ 8 Rechte und Pflichten der Mitglieder</h2>

  <h3>8.1 Rechte ordentlicher Mitglieder</h3>
  <ul>
    <li>Energiebezug bzw. Energieeinspeisung als teilnehmende:r Netzbenutzer:in nach Maßgabe der mit dem Verein abgeschlossenen Vereinbarung;</li>
    <li>Stimmrecht in der Mitgliederversammlung;</li>
    <li>passives Wahlrecht in den Vorstand und die Rechnungsprüfung;</li>
    <li>Teilnahme an Veranstaltungen des Vereins.</li>
  </ul>

  <h3>8.2 Rechte außerordentlicher Mitglieder und Ehrenmitglieder</h3>
  <p>Außerordentliche Mitglieder und Ehrenmitglieder haben das Recht zur Teilnahme an Veranstaltungen, jedoch kein Stimmrecht.</p>

  <h3>8.3 Pflichten der Mitglieder</h3>
  <ul>
    <li>Pünktliche Zahlung der Mitgliedsbeiträge;</li>
    <li>Zahlung der bezogenen Energie zu den vom Vorstand festgelegten Tarifen;</li>
    <li>Einhaltung der Statuten sowie der Beschlüsse des Vorstandes und der Mitgliederversammlung;</li>
    <li>Mitwirkung an den für die Abwicklung der EEG erforderlichen Datenfreigaben gegenüber dem Netzbetreiber.</li>
  </ul>

  <h2>§ 9 Finanzverfassung</h2>

  <h3>9.1 Mitgliedsbeitrag</h3>
  <p>Für ordentliche und außerordentliche Mitglieder besteht die Verpflichtung zur Leistung eines Mitgliedsbeitrags, dessen Höhe der Vorstand festlegt.</p>
  <p>Die Höhe des Mitgliedsbeitrags bewegt sich innerhalb der Bandbreite von EUR 0 bis EUR 120 pro Mitglied und Kalenderjahr. Eine Änderung der konkreten Beitragshöhe innerhalb dieser Bandbreite erfolgt durch Vorstandsbeschluss und bedarf keiner Statutenänderung.</p>
  <p>Die Beitragshöhe wird den Mitgliedern schriftlich (E-Mail zulässig) zur Kenntnis gebracht.</p>
  <p>Der Mitgliedsbeitrag wird quartalsweise oder jährlich – nach Wahl des Vorstandes – gemeinsam mit der Energieabrechnung in Rechnung gestellt. Die Verrechnung erfolgt spätestens 60 Tage nach Ablauf des jeweiligen Abrechnungszeitraums, um die Korrekturfristen des Netzbetreibers bzw. Energieversorgungsunternehmens zu berücksichtigen.</p>
  <p>Ehrenmitglieder sind von der Zahlung des Mitgliedsbeitrags befreit.</p>

  <h3>9.2 Energiepreise</h3>
  <p>Der Vorstand legt die Erzeuger- und Verbrauchertarife für die innerhalb der EEG verteilte Energie fest.</p>
  <p>Die Tarife sind so festzulegen, dass:</p>
  <ul>
    <li>der Verein im Hauptzweck nicht auf finanziellen Gewinn gerichtet ist (§ 79 Abs 2 EAG);</li>
    <li>die Zahlungsfähigkeit des Vereins jederzeit sichergestellt ist;</li>
    <li>ausreichende Liquiditätsvorsorge und Rücklagen gebildet werden können;</li>
    <li>die sachliche Gleichbehandlung der Mitglieder gewahrt ist.</li>
  </ul>
  <p>Die jeweils aktuell gültigen Tarife werden den Mitgliedern schriftlich (E-Mail zulässig) zur Kenntnis gebracht und sind Bestandteil der mit den Mitgliedern abgeschlossenen Energie- und Leistungsbezugsvereinbarungen.</p>

  <h3>9.3 Überschussenergie</h3>
  <p>Der Verein ist als Überschusseinspeiser-Gemeinschaft konzipiert. Energie, die nicht innerhalb der EEG verbraucht werden kann, wird über die individuellen Netzzugangsverträge der erzeugenden Mitglieder ins Netz eingespeist. Der Verein ist hierbei nicht involviert und vereinnahmt keine Erlöse aus dem Verkauf von Überschussenergie an Dritte.</p>

  <h2>§ 10 Vereinsorgane</h2>
  <p>Organe des Vereins sind:</p>
  <ul>
    <li>die Mitgliederversammlung (§§ 10 und 11);</li>
    <li>der Vorstand, bestehend aus Obmann/Obfrau und Kassier:in (§§ 12 bis 14);</li>
    <li>die Rechnungsprüfer:innen (§ 15);</li>
    <li>die Schlichtungseinrichtung gemäß § 8 VerG (§ 16).</li>
  </ul>

  <h2>§ 11 Die Mitgliederversammlung</h2>

  <h3>11.1 Ordentliche Mitgliederversammlung</h3>
  <p>Die ordentliche Mitgliederversammlung findet mindestens alle vier Jahre (§ 5 Abs 2 VerG) statt. Die Einberufung erfolgt durch den Vorstand.</p>

  <h3>11.2 Außerordentliche Mitgliederversammlung</h3>
  <p>Eine außerordentliche Mitgliederversammlung findet binnen 4 Wochen statt auf:</p>
  <ul>
    <li>Beschluss des Vorstandes;</li>
    <li>schriftlichen Antrag von mindestens einem Zehntel der stimmberechtigten Mitglieder (§ 5 Abs 2 VerG);</li>
    <li>Verlangen der Rechnungsprüfer:innen (§ 21 Abs 5 VerG);</li>
    <li>Beschluss eines/einer gerichtlich bestellten Kurators/Kuratorin.</li>
  </ul>

  <h3>11.3 Einberufung</h3>
  <p>Die Einladung zur Mitgliederversammlung erfolgt mindestens 14 Tage vor dem Termin schriftlich (E-Mail an die zuletzt bekannte Adresse zulässig) unter Angabe der Tagesordnung.</p>
  <p>Mitgliederversammlungen können präsentisch, virtuell (über elektronische Kommunikationsmittel) oder als Hybridveranstaltung durchgeführt werden.</p>

  <h3>11.4 Stimmrecht</h3>
  <p>Stimmberechtigt sind ausschließlich ordentliche Mitglieder. Jedem stimmberechtigten Mitglied kommt eine Stimme zu. Eine Stimmübertragung auf ein anderes Mitglied mittels schriftlicher Vollmacht ist zulässig; je Mitglied kann jedoch nur eine fremde Stimme übernommen werden.</p>

  <h3>11.5 Beschlussfassung</h3>
  <p>Die Mitgliederversammlung ist ohne Rücksicht auf die Anzahl der Erschienenen beschlussfähig. Beschlüsse werden mit einfacher Stimmenmehrheit gefasst. Bei Stimmengleichheit gibt die Stimme des/der Obmanns/Obfrau den Ausschlag.</p>
  <p>Beschlüsse über Statutenänderungen und freiwillige Auflösung des Vereins bedürfen einer Zweidrittelmehrheit der abgegebenen gültigen Stimmen.</p>

  <h2>§ 12 Aufgaben der Mitgliederversammlung</h2>
  <p>Der Mitgliederversammlung sind ausschließlich die folgenden, vereinsrechtlich zwingend zugewiesenen Aufgaben vorbehalten:</p>
  <ul>
    <li>Entgegennahme und Genehmigung des Rechenschaftsberichts und des Rechnungsabschlusses unter Einbindung der Rechnungsprüfer:innen;</li>
    <li>Wahl und Enthebung der Mitglieder des Vorstandes;</li>
    <li>Wahl und Enthebung der Rechnungsprüfer:innen;</li>
    <li>Entlastung des Vorstandes;</li>
    <li>Beschlussfassung über Statutenänderungen;</li>
    <li>Beschlussfassung über die freiwillige Auflösung des Vereins;</li>
    <li>Genehmigung von Rechtsgeschäften zwischen Vorstandsmitgliedern und dem Verein, soweit sie nicht zu marktkonformen Bedingungen erfolgen;</li>
    <li>Entscheidung über Berufungen gegen Aufnahmeablehnungen, soweit diese nicht zuvor durch die Schlichtungseinrichtung erledigt sind.</li>
  </ul>
  <p>Alle übrigen Angelegenheiten – insbesondere die laufende Geschäftsführung, die Aufnahme und der Ausschluss von Mitgliedern, die Festlegung von Mitgliedsbeiträgen und Energiepreisen sowie der gesamte operative Betrieb der EEG – obliegen dem Vorstand.</p>

  <h2>§ 13 Der Vorstand</h2>

  <h3>13.1 Zusammensetzung</h3>
  <p>Der Vorstand besteht aus zwei Mitgliedern:</p>
  <ul>
    <li>Obmann/Obfrau (verpflichtend);</li>
    <li>Kassier:in (verpflichtend).</li>
  </ul>
  <p>Bei Bedarf kann die Mitgliederversammlung ein drittes Vorstandsmitglied als Schriftführer:in bestellen.</p>

  <h3>13.2 Funktionsperiode</h3>
  <p>Die Funktionsperiode des Vorstandes beträgt 3 Jahre. Wiederwahl ist unbeschränkt zulässig. Jede Funktion ist persönlich auszuüben.</p>

  <h3>13.3 Kooptierung</h3>
  <p>Fällt ein Vorstandsmitglied während der Funktionsperiode aus, ist der verbleibende Vorstand berechtigt, ein neues Vorstandsmitglied zu kooptieren. Die nachträgliche Bestätigung durch die nächste Mitgliederversammlung ist einzuholen.</p>

  <h3>13.4 Beschlussfassung</h3>
  <p>Der Vorstand fasst seine Beschlüsse mit einfacher Stimmenmehrheit; bei Stimmengleichheit gibt die Stimme des Obmannes/der Obfrau den Ausschlag. Die Beschlussfassung kann auch im Umlaufweg (schriftlich, per E-Mail) erfolgen.</p>

  <h2>§ 14 Aufgaben des Vorstandes</h2>

  <h3>14.1 Generalkompetenz</h3>
  <p>Dem Vorstand obliegt die gesamte Leitung des Vereins. Er ist das Leitungsorgan im Sinne des VerG 2002. Alle Aufgaben, die nicht in § 11 ausdrücklich der Mitgliederversammlung vorbehalten sind, fallen in die ausschließliche Zuständigkeit des Vorstandes.</p>
  <p>Dies umfasst insbesondere:</p>
  <ul>
    <li>Aufnahme neuer Mitglieder (§ 5.3);</li>
    <li>Ausschluss von Mitgliedern (§ 6.5);</li>
    <li>Festlegung der Mitgliedsbeiträge innerhalb der in § 8.1 vorgegebenen Bandbreite;</li>
    <li>Festlegung der Energiepreise (Erzeuger- und Verbrauchertarif);</li>
    <li>Abschluss von Energie- und Leistungsbezugsvereinbarungen mit den Mitgliedern;</li>
    <li>Einrichtung eines den Anforderungen des Vereins entsprechenden Rechnungswesens mit laufender Aufzeichnung der Einnahmen/Ausgaben und Führung eines Vermögensverzeichnisses;</li>
    <li>Erstellung des Jahresvoranschlags, des Rechenschaftsberichts und des Rechnungsabschlusses;</li>
    <li>Vorbereitung und Einberufung der Mitgliederversammlung;</li>
    <li>Information der Vereinsmitglieder über die Vereinstätigkeit, die Vereinsgebarung und den geprüften Rechnungsabschluss;</li>
    <li>Verwaltung des Vereinsvermögens;</li>
    <li>Aufnahme und Kündigung allfälliger Dienstnehmer:innen sowie Abschluss von Werkverträgen;</li>
    <li>Beauftragung externer Dienstleister zur Abwicklung der EEG;</li>
    <li>Vertretung gegenüber dem Netzbetreiber (Kärnten Netz GmbH), gegenüber der EDA-Plattform sowie sonstigen Behörden und Vertragspartnern;</li>
    <li>Bekanntgabe einer Statutenänderung, die Einfluss auf die abgabenrechtlichen Begünstigungen hat, an das zuständige Finanzamt binnen einer Frist von einem Monat.</li>
  </ul>

  <h3>14.2 Vertretung nach außen</h3>
  <p>Der Verein wird nach außen grundsätzlich durch den/die Obmann/Obfrau alleine vertreten. In Geldangelegenheiten über EUR 1.000,00 im Einzelfall bedürfen schriftliche Ausfertigungen des Vereins zu ihrer Gültigkeit der gemeinsamen Unterschrift des Obmannes/der Obfrau und des Kassiers/der Kassierin.</p>
  <p>Rechtsgeschäfte zwischen Vorstandsmitgliedern und dem Verein, die nicht zu marktkonformen Bedingungen erfolgen, bedürfen der Zustimmung der Mitgliederversammlung.</p>

  <h2>§ 15 Rechnungsprüfer:innen</h2>
  <p>Mindestens zwei Rechnungsprüfer:innen werden von der Mitgliederversammlung auf die Dauer von 3 Jahren gewählt. Wiederwahl ist unbeschränkt zulässig.</p>
  <p>Die Rechnungsprüfer:innen müssen nicht Vereinsmitglied sein und dürfen keinem anderen Vereinsorgan – mit Ausnahme der Mitgliederversammlung – angehören, dessen Tätigkeit Gegenstand der Prüfung ist (§ 5 Abs 5 VerG). Insbesondere dürfen Rechnungsprüfer:innen nicht zugleich Vorstandsmitglieder sein.</p>
  <p>Ihnen obliegen die laufende Geschäftskontrolle sowie die Prüfung der Finanzgebarung des Vereins im Hinblick auf die Ordnungsmäßigkeit der Rechnungslegung und die statutengemäße Verwendung der Mittel. Sie berichten dem Vorstand und der Mitgliederversammlung.</p>

  <h2>§ 16 Schlichtungseinrichtung</h2>

  <h3>16.1 Vereinsinterne Streitschlichtung gemäß § 8 VerG</h3>
  <p>Zur Schlichtung von Streitigkeiten aus dem Vereinsverhältnis ist eine vereinsinterne Schlichtungseinrichtung eingerichtet. Sie ist kein Schiedsgericht im Sinne der §§ 577 ff ZPO.</p>

  <h3>16.2 Zusammensetzung</h3>
  <p>Die Schlichtungseinrichtung besteht aus drei Mitgliedern. Jede der beiden Streitparteien nominiert binnen 14 Tagen ab Anrufung schriftlich ein Mitglied. Die beiden so nominierten Mitglieder wählen einvernehmlich ein drittes Mitglied als Vorsitzende:n.</p>
  <p>Kommt eine Einigung über den/die Vorsitzende:n binnen 14 Tagen nicht zustande, wird diese:r durch das Los aus den Vorschlägen der beiden Streitparteien bestimmt.</p>
  <p>Die Mitglieder der Schlichtungseinrichtung dürfen nicht selbst Partei des Streites sein.</p>

  <h3>16.3 Verfahren</h3>
  <p>Die Schlichtungseinrichtung trifft ihre Entscheidung nach Gewährung beiderseitigen Gehörs mit einfacher Stimmenmehrheit nach bestem Wissen und Gewissen. Ihre Entscheidungen sind vereinsintern endgültig; der ordentliche Rechtsweg ist nach Ausschöpfung des vereinsinternen Instanzenzugs (über 6 Monate ab Anrufung) zulässig.</p>

  <h2>§ 17 Datenschutz</h2>
  <p>Der Verein verpflichtet sich, die ihm in Ausübung der Mitgliedschaftsverhältnisse und des Vereinszwecks zur Kenntnis gelangenden personenbezogenen Daten (insbesondere Name, Geburtsdatum, Adresse, Zählpunktnummer, Energieerzeugungs- und Verbrauchsdaten) gemäß DSGVO und Datenschutzgesetz (DSG) mit höchster Vertraulichkeit zu behandeln.</p>
  <p>Die Verarbeitung erfolgt ausschließlich zur Erfüllung der vertraglichen Pflichten gegenüber den Mitgliedern und zur Abwicklung des Vereinszwecks. Eine Weitergabe an Dritte erfolgt nur, soweit dies zur Erfüllung des Vereinszwecks erforderlich ist (insbesondere an Kärnten Netz GmbH und die EDA-Plattform).</p>
  <p>Für die Verarbeitung durch externe Dienstleister schließt der Verein entsprechende Auftragsverarbeitungsverträge ab.</p>
  <p>Der Verein erstellt eine eigene Datenschutzerklärung, die den Mitgliedern bei Beitritt zur Verfügung gestellt wird.</p>
  <p>Im Prinzip sind die Daten nach erfolgter Abrechnung und mit Ablauf allfälliger Aufbewahrungspflichten (insbesondere § 132 BAO: 7 Jahre) zu löschen.</p>

  <h2>§ 18 Auflösung des Vereins</h2>

  <h3>18.1 Freiwillige Auflösung</h3>
  <p>Die freiwillige Auflösung des Vereins kann nur in einer eigens zu diesem Zweck einberufenen Mitgliederversammlung und nur mit Zweidrittelmehrheit der abgegebenen gültigen Stimmen beschlossen werden.</p>

  <h3>18.2 Verwendung des Vereinsvermögens</h3>
  <p>Das nach Abdeckung der Passiva verbleibende Vereinsvermögen ist für Zwecke der Förderung erneuerbarer Energien zu verwenden.</p>
  <p>Soweit möglich und zulässig, soll es einer anderen Erneuerbare-Energie-Gemeinschaft im Sinne des § 79 EAG, einer Bürgerenergiegemeinschaft im Sinne des § 16b ElWOG 2010 oder einem Verein bzw. einer Körperschaft zufallen, die gleiche oder ähnliche Zwecke verfolgt.</p>
  <p>Die Mitglieder dürfen bei Auflösung oder Aufhebung des Vereins keine Anteile am Vereinsvermögen erhalten, das über ihre allfällig geleisteten und rückforderbaren Kapitaleinlagen hinausgeht. Da im vorliegenden Verein keine Grundeinlage und kein Eintrittsgeld erhoben wird, verbleibt das gesamte Restvermögen bei der gemäß Abs. 18.2 begünstigten Einrichtung.</p>

  <h3>18.3 Vermögen bei Ausscheiden von Mitgliedern</h3>
  <p>Im Falle des Ausscheidens oder Ausschlusses eines Mitglieds verbleiben bereits geleistete Mitgliedsbeiträge und sonstige Zuwendungen entschädigungslos beim Verein.</p>

  <h3>18.4 Behördliche Anzeige</h3>
  <p>Die freiwillige Auflösung sowie der Beschluss über die Verwendung des Vermögens sind binnen 4 Wochen nach der Beschlussfassung der zuständigen Vereinsbehörde anzuzeigen (§ 28 VerG).</p>

  <h2>§ 19 Schlussbestimmungen</h2>
  <p>Soweit in diesen Statuten personenbezogene Bezeichnungen nur in männlicher oder weiblicher Form angeführt sind, beziehen sie sich auf Personen jeglichen Geschlechts in gleicher Weise.</p>
  <p>Sollten einzelne Bestimmungen dieser Statuten unwirksam sein oder werden, so berührt dies die Wirksamkeit der übrigen Bestimmungen nicht. An die Stelle der unwirksamen Bestimmung tritt eine wirksame Regelung, die dem mit der unwirksamen Bestimmung verfolgten Zweck wirtschaftlich am nächsten kommt.</p>

